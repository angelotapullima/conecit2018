package com.conecit.angelo.conecit2018.model;

public class DatosConcursos {
    private String tituloconcursos;
    private String descripcionconcursos;
    private String imagenconcursos;

    public DatosConcursos() {
        this.tituloconcursos = tituloconcursos;
        this.descripcionconcursos = descripcionconcursos;
        this.imagenconcursos = imagenconcursos;
    }

    public String getTituloconcursos() {
        return tituloconcursos;
    }

    public void setTituloconcursos(String tituloconcursos) {
        this.tituloconcursos = tituloconcursos;
    }

    public String getDescripcionconcursos() {
        return descripcionconcursos;
    }

    public void setDescripcionconcursos(String descripcionconcursos) {
        this.descripcionconcursos = descripcionconcursos;
    }

    public String getImagenconcursos() {
        return imagenconcursos;
    }

    public void setImagenconcursos(String imagenconcursos) {
        this.imagenconcursos = imagenconcursos;
    }
}
