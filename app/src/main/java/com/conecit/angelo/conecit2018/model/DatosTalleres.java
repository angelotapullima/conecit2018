package com.conecit.angelo.conecit2018.model;

public class DatosTalleres {
    private String imagenTalleres;
    private String tituloTaller;
    private String duracionTaller;
    private String ponenteTaller;
    private String precioTaller;
    private String descripcionTaller;
    private String fotoPonente;

    public DatosTalleres() {
        this.imagenTalleres = imagenTalleres;
        this.tituloTaller = tituloTaller;
        this.duracionTaller = duracionTaller;
        this.ponenteTaller = ponenteTaller;
        this.precioTaller = precioTaller;
        this.descripcionTaller = descripcionTaller;
        this.fotoPonente = fotoPonente;
    }

    public String getImagenTalleres() {
        return imagenTalleres;
    }

    public void setImagenTalleres(String imagenTalleres) {
        this.imagenTalleres = imagenTalleres;
    }

    public String getTituloTaller() {
        return tituloTaller;
    }

    public void setTituloTaller(String tituloTaller) {
        this.tituloTaller = tituloTaller;
    }

    public String getDuracionTaller() {
        return duracionTaller;
    }

    public void setDuracionTaller(String duracionTaller) {
        this.duracionTaller = duracionTaller;
    }

    public String getPonenteTaller() {
        return ponenteTaller;
    }

    public void setPonenteTaller(String ponenteTaller) {
        this.ponenteTaller = ponenteTaller;
    }

    public String getPrecioTaller() {
        return precioTaller;
    }

    public void setPrecioTaller(String precioTaller) {
        this.precioTaller = precioTaller;
    }

    public String getDescripcionTaller() {
        return descripcionTaller;
    }

    public void setDescripcionTaller(String descripcionTaller) {
        this.descripcionTaller = descripcionTaller;
    }

    public String getFotoPonente() {
        return fotoPonente;
    }

    public void setFotoPonente(String fotoPonente) {
        this.fotoPonente = fotoPonente;
    }
}

